CREATE PROCEDURE AddUtoA
 @ID_USER INT,
 @ID_ACHIEVEMENT INT
AS
BEGIN
	INSERT INTO User_To_Achievement([ID_USER],[ID_ACHIEVEMENT]) 
	VALUES(@ID_USER, @ID_ACHIEVEMENT)
END

