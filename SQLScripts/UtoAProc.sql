CREATE PROCEDURE RemoveUtoA
@ID_USER INT,
@ID_ACHIEVEMENT INT
AS
BEGIN
	DELETE FROM User_To_Achievement
	WHERE ID_USER = @ID_USER AND ID_ACHIEVEMENT = @ID_ACHIEVEMENT
END

